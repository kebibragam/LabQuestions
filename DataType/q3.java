package Java.LabQuestions.DataType;

public class q3 {
    public static void main(String[] args) {
        int a=7;
		int i,j;
		i=a>>1;
		j =a<<1;
		System.out.println("the value of a before right shfit is: " +a);
		System.out.println("the value of a after applying right shfit is: " +i);
		System.out.println("the value of a after applying left shfit is: " +j);
    }
}
